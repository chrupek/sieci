package com.chrupek.sieci.serwer.core;

public class Coordinates{
	public int x;
	public int y;
	
	/**
	 * 
	 * @param x
	 * @param y
	 */
	public Coordinates(int x, int y){
		this.x = x;
		this.y = y;
	}
			
	/**
	 * 
	 * @param x
	 * @param y
	 */
	public void setCoordinates(int x, int y){
		this.x = x;
		this.y = y;
	}
}
